public class NumToWord {
        public static void main (String[]args)
        {
        int x = 1;
        if ( x == 1 )
        {
            System.out.print("Unit");
        }
        else if ( x == 10 )
        {
            System.out.print("Ten");
        }
        else if ( x == 100 )
        {
            System.out.print("Hundred");
        }
        else if ( x == 1000 )
        {
            System.out.print("Thousand");
        }
        }
}
